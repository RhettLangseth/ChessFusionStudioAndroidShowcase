fun normalizeAppThemeColorName(value: String): String {
    return when (value.trim()) {
        APP_THEME_COLOR_SYSTEM,
        "Classic" -> APP_THEME_COLOR_SYSTEM
        APP_THEME_COLOR_AQUA,
        "Blue" -> APP_THEME_COLOR_AQUA
        APP_THEME_COLOR_LEAF,
        "Green" -> APP_THEME_COLOR_LEAF
        APP_THEME_COLOR_HONEY,
        "Amber" -> APP_THEME_COLOR_HONEY
        APP_THEME_COLOR_HEARTH,
        "Red" -> APP_THEME_COLOR_HEARTH
        APP_THEME_COLOR_SLATE -> APP_THEME_COLOR_SLATE
        else -> APP_THEME_COLOR_SYSTEM
    }
}

fun resolveAppColorScheme(themeColorName: String, darkTheme: Boolean): ColorScheme {
    val normalizedThemeColor = normalizeAppThemeColorName(themeColorName)
    return when (normalizedThemeColor) {
        APP_THEME_COLOR_AQUA -> if (darkTheme) AquaDarkColorScheme else AquaLightColorScheme
        APP_THEME_COLOR_LEAF -> if (darkTheme) LeafDarkColorScheme else LeafLightColorScheme
        APP_THEME_COLOR_HONEY -> if (darkTheme) HoneyDarkColorScheme else HoneyLightColorScheme
        APP_THEME_COLOR_HEARTH -> if (darkTheme) HearthDarkColorScheme else HearthLightColorScheme
        APP_THEME_COLOR_SLATE -> if (darkTheme) SlateDarkColorScheme else SlateLightColorScheme
        else -> if (darkTheme) SystemDarkColorScheme else SystemLightColorScheme
    }
}

